function booleanToString(b) {
    // Using toString method
    // return b.toString();

    // Using concatenation with an empty string
    return '' + b;
}

// Example usage:
console.log(booleanToString(true));  // Output: "true"
console.log(booleanToString(false)); // Output: "false"

